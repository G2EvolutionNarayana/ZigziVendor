package xplotica.littlekites.Activity_parent;

public class parent_ObjectDrawerItem {


    private int icon;
    private String name;

    public parent_ObjectDrawerItem(int icon, String name) {
        this.icon = icon;
        this.name = name;

    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
